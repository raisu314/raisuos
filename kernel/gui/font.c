void font_init(void) { /* Fonts embedded directly */ }

void font_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg) {
  if ((uint8_t)c >= 128)
    c = '?';

  for (int row = 0; row < 8; row++) {
    uint8_t row_data = font8x8[(uint8_t)c][row];
    for (int col = 0; col < 8; col++) {
      if (row_data & (1 << (7 - col))) {
        vbe_set_pixel(x + col, y + row, fg);
      } else if (bg != 0) {
        /* If bg is 0, render transparent */
        vbe_set_pixel(x + col, y + row, bg);
      }
    }
  }
}

void font_draw_string(int x, int y, const char *str, uint32_t fg, uint32_t bg) {
  int cx = x;
  while (*str) {
    if (*str == '\n') {
      cx = x;
      y += 10;
    } else {
      font_draw_char(cx, y, *str, fg, bg);
      cx += 8;
    }
    str++;
  }
}

void font_draw_char_buffer(uint32_t *buffer, int bw, int x, int y, char c,
                           uint32_t fg, uint32_t bg) {
  if ((uint8_t)c >= 128)
    c = '?';
  for (int row = 0; row < 8; row++) {
    uint8_t row_data = font8x8[(uint8_t)c][row];
    for (int col = 0; col < 8; col++) {
      if (row_data & (1 << (7 - col))) {
        buffer[(y + row) * bw + (x + col)] = fg;
      } else if (bg != 0) {
        buffer[(y + row) * bw + (x + col)] = bg;
      }
    }
  }
}

void font_draw_string_buffer(uint32_t *buffer, int bw, int x, int y,
                             const char *str, uint32_t fg, uint32_t bg) {
  int cx = x;
  while (*str) {
    if (*str == '\n') {
      cx = x;
      y += 10;
    } else {
      font_draw_char_buffer(buffer, bw, cx, y, *str, fg, bg);
      cx += 8;
    }
    str++;
  }
}
